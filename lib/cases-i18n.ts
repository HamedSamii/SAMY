function mergeMetricLabels(
  metrics: { value: string; label: string; color: MetricColor }[],
  labels: string[],
) {
  return metrics.map((m, i) => ({ ...m, label: labels[i] ?? m.label }));
}

function mergeDetail(study: CaseStudyDetail, overlay: CaseArOverlay["detail"]): CaseStudyDetail {
  return {
    ...study,
    kicker: overlay.kicker,
    title: overlay.title,
    background: overlay.background,
    challenges: overlay.challenges,
    flow: overlay.flow,
    tags: overlay.tags,
    metrics: mergeMetricLabels(study.metrics, overlay.metricLabels),
  };
}

