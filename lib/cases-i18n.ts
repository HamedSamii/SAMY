function applyOverlay(card: CaseIndexCard, overlay: CaseArOverlay): CaseIndexCard {
  return {
    ...card,
    industryLabel: overlay.industryLabel,
    listTitle: overlay.listTitle,
    listExcerpt: overlay.listExcerpt,
    metrics: mergeMetricLabels(card.metrics, overlay.metricLabels),
    tags: overlay.tags,
  };
}

export function localizeCaseCard(card: CaseIndexCard, lang: Lang): CaseIndexCard {
  if (lang === "en") return card;
  const overlay = caseArOverlays[card.id];
  if (!overlay) return card;
  return applyOverlay(card, overlay);
}

export function localizeCaseStudy(study: CaseStudy, lang: Lang): CaseStudy {
  if (lang === "en") return study;
  const overlay = caseArOverlays[study.id];
  if (!overlay) return study;
  return {
    ...applyOverlay(study, overlay),
    detail: mergeDetail(study.detail, overlay.detail),
  };
}

export function getLocalizedCaseById(id: string, lang: Lang): CaseStudy | null {
  const study = getCaseById(id);
  return study ? localizeCaseStudy(study, lang) : null;
}

